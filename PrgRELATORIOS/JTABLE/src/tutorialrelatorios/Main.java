    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String caminhoRelatorio = this.getClass()
                    .getResource(FOLDER_RELATORIOS).getPath();

            //Nome do Relatorio
            String nomeRelatorioJasper = "example";

            /* caminho completo até o relatório */
            String sourceFileName = caminhoRelatorio + File.separator + nomeRelatorioJasper + ".jrxml";

            JasperReport jr = JasperCompileManager.compileReport(sourceFileName);
            
            Map<String, Object> params = new HashMap<String, Object>();
//            params.put("titulo", "RELATORIO COM TABELA");

            DefaultTableModel de = (DefaultTableModel) jTable1.getModel();
            JRTableModelDataSource datasource = new JRTableModelDataSource(de);

            JasperPrint jp = JasperFillManager.fillReport(jr, params, datasource);

            JasperViewer.viewReport(jp, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

